public class Entity {

    protected int x;
    protected int y;
    protected int z;

    public Entity(){
        x=0;
        y=0;
        z=0;
    }

    public Entity(int x, int y, int z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }
}
